int main(int argc, char const* argv[])
{
    read(0, &buf, sizeof buf);
    memcpy(&dst, &buf, sizeof buf);

    for (int i = 1; i < sizeof(data); ++i)
        data[i - 1] ^= data[i];

    if (memcmp(dst, data, sizeof buf) == 0)
        return 1;
    return 0;
}
